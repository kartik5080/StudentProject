package printstudentdetails;

/**
 *model class- raw detail for student.
 * @author Asus
 */
public class Student 
{ // encapsulation principle- creating private variable, getter an setters.
    private String name;
    private int id;
    Student()
    {
        
    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }
    
    
    
}
